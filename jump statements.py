#Jump statements

#1. Break

for i in range(5):
    if i == 3:
        break
    print("I am ",i)
print("Hey, I am break out!!!")

#2 Exit

for i in range(5):
  if i == 3:
      exit()
  print("I am ",i)
print("Hey, I am exit out!!!")


#3 Continue

for i in range(5):
  if i == 3:
      continue
  print("I am ",i)


